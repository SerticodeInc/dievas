// Border radius primitives.
// Applied to containers, cards, buttons, inputs, and overlays.
// `full` is used for pill shapes and circular avatars.

abstract final class DievasRadiusPrimitives {
  static const double none = 0.0;
  static const double xs = 2.0;
  static const double sm = 4.0;
  static const double md = 8.0;
  static const double lg = 12.0;
  static const double xl = 16.0;
  static const double x2l = 20.0;
  static const double x3l = 24.0;

  /// pill / circle
  static const double full = 9999.0;
}
